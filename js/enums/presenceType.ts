// The types of user presence.
enum PresenceType {
  // The user is offline.
  Offline = 'Offline',

  // The user is online.
  Online = 'Online',

  // The user is currently in an experience.
  Experience = 'Experience',

  // The user is currently in Roblox Studio.
  Studio = 'Studio',
}

export default PresenceType;
